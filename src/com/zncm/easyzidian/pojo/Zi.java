package com.zncm.easyzidian.pojo;

public class Zi {
	private String id;
	private String zi;
	private String bh;
	private String py;
	private String py2;
	private String bs;
	private String js;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getZi() {
		return zi;
	}

	public void setZi(String zi) {
		this.zi = zi;
	}

	public String getBh() {
		return bh;
	}

	public void setBh(String bh) {
		this.bh = bh;
	}

	public String getPy() {
		return py;
	}

	public void setPy(String py) {
		this.py = py;
	}

	public String getPy2() {
		return py2;
	}

	public void setPy2(String py2) {
		this.py2 = py2;
	}

	public String getBs() {
		return bs;
	}

	public void setBs(String bs) {
		this.bs = bs;
	}

	public String getJs() {
		return js;
	}

	public void setJs(String js) {
		this.js = js;
	}

	@Override
	public String toString() {
		return "Zi [id=" + id + ", zi=" + zi + ", bh=" + bh + ", py=" + py
				+ ", py2=" + py2 + ", bs=" + bs + ", js=" + js + "]";
	}

}
